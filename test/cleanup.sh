#!/bin/bash
# set -e
IMG=esl-test-0001
echo "---------------------------------------------------------------------------"

# Postmortem
for t in client server; do
  echo "---------------------------------------------------------------------------"
  echo "----------------- $t --------------------------------------------------"
  echo "---------------------------------------------------------------------------"
  docker logs $IMG-$t > $IMG-$t.log;
done
for t in client server; do
  echo "---------------------------------------------------------------------------"
  echo "----------------- $t --------------------------------------------------"
  echo "---------------------------------------------------------------------------"
  docker kill $IMG-$t;
  docker rm $IMG-$t;
  docker rmi $IMG-$t;
done
